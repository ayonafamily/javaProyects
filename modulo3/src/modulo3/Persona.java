package modulo3;

public class Persona {
private	String nombre;
private	String pais;
private	int edad;
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public void setPais(String pais) {
		this.pais = pais;
	}
	
	public void setEdad( int edad) {
		this.edad = edad;
	}
	
	
	public String getNombre( ) {
		return nombre;
	}
	
	public String getPais( ) {
		return pais;
	}
	
	public int getEdad(  ) {
		return edad;
	}
	
}


